function applyLang(lang) {
  const t = translations[lang];
  const cfg = langConfig[lang];
  if (!t || !cfg) return;

  // Document direction & lang
  document.documentElement.lang = lang;
  document.documentElement.dir = cfg.dir;

  // Translate all marked elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (t[key] === undefined) return;
    if (el.tagName === 'H1') {
      const cursor = el.querySelector('.cursor');
      el.innerHTML = t[key] + (cursor ? cursor.outerHTML : '');
    } else {
      el.innerHTML = t[key];
    }
  });

  if (t["page.title"]) document.title = t["page.title"];

  // Update dropdown display
  const flagEl = document.getElementById('langFlag');
  const labelEl = document.getElementById('langCode');
  if (flagEl) flagEl.textContent = cfg.flag;
  if (labelEl) labelEl.textContent = lang.toUpperCase();

  // Highlight active item in menu
  document.querySelectorAll('.lang-option').forEach(li => {
    li.classList.toggle('active', li.dataset.lang === lang);
  });

  localStorage.setItem('preferred-lang', lang);
}

// Build the dropdown menu from langConfig
function buildDropdown() {
  const menu = document.getElementById('langMenu');
  if (!menu) return;
  Object.entries(langConfig).forEach(([code, cfg]) => {
    const li = document.createElement('li');
    li.className = 'lang-option';
    li.dataset.lang = code;
    li.innerHTML = `<span class="opt-flag">${cfg.flag}</span><span class="opt-label">${cfg.label}</span>`;
    li.addEventListener('click', () => {
      applyLang(code);
      closeDropdown();
    });
    menu.appendChild(li);
  });
}

function closeDropdown() {
  const switcher = document.getElementById('langSwitcher');
  if (switcher) switcher.classList.remove('open');
}

// Expose
window.applyLang = applyLang;
window.buildDropdown = buildDropdown;
window.closeDropdown = closeDropdown;
window.translations = translations;
window.langConfig = langConfig;
